<?php

namespace App\Listeners;

use App\Events\UserRegistered;

class CreateDefaultProfile
{
    /**
     * Create the event listener.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Handle the event.
     *
     * @param  UserRegistered  $event
     * @return void
     */
    public function handle(UserRegistered $event)
    {
        $user = $event->user;
        
        $user->profile()->create();
    }
}
